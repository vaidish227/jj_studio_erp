// =====================================================================
//  TRIGGER THANK YOU AUTOMATION
// =====================================================================
const triggerThankYouAutomation = async (req, res) => {
  try {
    const { id } = req.params;
    const client = await CRMClient.findById(id);

    if (!client) {
      return res.status(404).json({ message: "Client not found" });
    }

    if (!client.email) {
      return res.status(400).json({ message: "Client email not found" });
    }

    await sendEmail({
      to: client.email,
      subject: "Thank You for Meeting with JJ Studio",
      html: getLeadTemplate(client.name),
    });

    client.automation = {
      ...client.automation,
      thankYouSentAt: new Date(),
    };
    client.lifecycleStage = "thank_you_sent";

    appendInteraction(client, {
      type: "thank_you",
      title: "Automated thank you sent",
      description: "The post-meeting thank you message was triggered.",
    });

    // Log communication
    client.communicationLogs = client.communicationLogs || [];
    client.communicationLogs.push({
      channel: "Email",
      direction: "Outbound",
      subject: "Thank You for Meeting with JJ Studio",
      status: "sent",
      timestamp: new Date(),
    });

    await client.save();

    res.status(200).json({
      message: "Thank you message sent successfully",
      client,
      lead: client,
    });
  } catch (error) {
    res.status(500).json({ message: error.message });
  }
};

