// =====================================================================
//  UPDATE CLIENT DETAILS (Client Info Form — enriches same record)
//  This is the key method: it takes enquiry-stage data and enriches it
//  with detailed client information (DOB, spouse details, site address, etc.)
// =====================================================================
const updateClientDetails = async (req, res) => {
  try {
    const { id } = req.params;

    if (!id) {
      return res.status(400).json({ message: "Client ID is required" });
    }

    const client = await CRMClient.findById(id);

    if (!client) {
      return res.status(404).json({ message: "Client not found" });
    }

    // Determine if this is the first client info submission
    const isFirstClientInfo = !client.clientInfoCompleted;

    // Merge all provided fields into the existing document
    const fieldsToUpdate = { ...req.body };

    // Remove fields that should not be directly overwritten
    delete fieldsToUpdate._id;
    delete fieldsToUpdate.trackingId;
    delete fieldsToUpdate.interactionHistory;
    delete fieldsToUpdate.communicationLogs;

    Object.assign(client, fieldsToUpdate);

    // Mark client info as completed if coming from client info form
    if (req.body.address || req.body.dob || req.body.companyName || req.body.spouse?.email) {
      client.clientInfoCompleted = true;
      if (isFirstClientInfo) {
        client.clientInfoCompletedAt = new Date();
      }
    }

    // Append timeline event
    appendInteraction(client, {
      type: "client_info",
      title: isFirstClientInfo
        ? "Client information submitted"
        : "Client information updated",
      description: isFirstClientInfo
        ? "Detailed client information was submitted and linked to the enquiry."
        : "Client profile information was updated.",
    });

    await client.save();

    console.log("Client updated:", client._id);

    res.status(200).json({
      message: "Client updated successfully",
      client,
      // Backward compat
      lead: client,
    });
  } catch (error) {
    console.log("Error updating client:", error.message);
    res.status(500).json({ message: error.message });
  }
};

// =====================================================================
//  UPDATE CLIENT STATUS (with lifecycle sync)
// =====================================================================
const updateClientStatus = async (req, res) => {
  try {
    const { id } = req.params;
    const { status, lifecycleStage } = req.body;

    if (!id) {
      return res.status(400).json({ message: "Client ID is required" });
    }

    if (!status && !lifecycleStage) {
      return res
        .status(400)
        .json({ message: "Status or lifecycleStage is required" });
    }

    if (status && !validStatus.includes(status)) {
      return res.status(400).json({ message: "Invalid status value" });
    }

    const client = await CRMClient.findById(id);

    if (!client) {
      return res.status(404).json({ message: "Client not found" });
    }

    const oldStatus = client.status;
    const oldLifecycle = client.lifecycleStage;

    if (status) {
      client.status = status;
    }

    if (lifecycleStage) {
      client.lifecycleStage = lifecycleStage;
    } else if (status) {
      // Auto-map lifecycle from status if not explicitly set
      const lifecycleByStatus = {
        new: "enquiry",
        contacted: "kit",
        meeting_done: "followup_due",
        proposal_sent: "proposal_sent",
        converted: "converted",
        lost: "lost",
      };
      if (lifecycleByStatus[status]) {
        client.lifecycleStage = lifecycleByStatus[status];
      }
    }

    appendInteraction(client, {
      type: "status_change",
      title: "Status updated",
      description: `Status: ${oldStatus} → ${client.status}. Lifecycle: ${oldLifecycle} → ${client.lifecycleStage}.`,
      metadata: { oldStatus, oldLifecycle, newStatus: client.status, newLifecycle: client.lifecycleStage },
    });

    await client.save();

    console.log("Client status updated:", client._id, client.status);

    res.status(200).json({
      message: "Client status updated successfully",
      client,
      lead: client,
    });
  } catch (error) {
    console.log("Error updating status:", error.message);
    res.status(500).json({ message: error.message });
  }
};

