// =====================================================================
//  CREATE CLIENT (Enquiry Form)
//  Creates a new CRMClient record with initial enquiry data.
//  Tracking ID is auto-generated by the pre-validate hook.
// =====================================================================

const createClientEnquiry = async (req, res) => {
  try {
    const { name, phone, email } = req.body;

    if (!name || !phone) {
      return res.status(400).json({
        message: "Name and phone are required",
      });
    }

    // Check duplicate client
    const duplicateQuery = [{ phone }];
    if (email) duplicateQuery.push({ email });

    const existingClient = await CRMClient.findOne({
      $or: duplicateQuery,
    });

    if (existingClient) {
      return res.status(400).json({
        message: "A client already exists with the same phone or email",
        existingId: existingClient._id,
        trackingId: existingClient.trackingId,
      });
    }

    // Create client data
    const clientData = {
      name: req.body.name,
      phone: req.body.phone,
      email: req.body.email,
      spouse: req.body.spouse || {},
      referredBy: req.body.referredBy,
      referrerPhone: req.body.referrerPhone,
      referrerEmail: req.body.referrerEmail,
      projectType: req.body.projectType,
      area: req.body.area,
      budget: req.body.budget,
      city: req.body.city,
      notes: req.body.notes,
      preferredMeetingDate: req.body.preferredMeetingDate || undefined,
      preferredMeetingTime: req.body.preferredMeetingTime || undefined,
      source: req.body.source || "walk_in",
      priority: req.body.priority || "medium",

      // CRM Flow
      status: "new",
      lifecycleStage: "enquiry",

      // WhatsApp Automation
      whatsappSent: false,

      clientInfoCompleted: false,

      interactionHistory: [
        {
          type: "status_change",
          title: "Enquiry captured",
          description:
            "A new enquiry was submitted and added to the CRM pipeline.",
        },
      ],
    };

    // Handle siteAddress
    if (req.body.siteAddress) {
      if (typeof req.body.siteAddress === "string") {
        clientData.siteAddress = {
          fullAddress: req.body.siteAddress,
        };
      } else {
        clientData.siteAddress = req.body.siteAddress;
      }
    }

    // Create Client
    const client = await CRMClient.create(clientData);

    console.log(
      `Lead created successfully for WhatsApp automation: ${client._id}`
    );

    console.log(
      "Client created:",
      client._id,
      "TrackingID:",
      client.trackingId
    );

    notify({
      type: "lead.created",
      module: "crm",
      priority: "high",
      title: `New lead: ${client.name}`,
      message: `${client.projectType || "Interior"} enquiry${client.city ? ` from ${client.city}` : ""}${client.source ? ` (via ${client.source})` : ""}.`,
      link: `/crm/leads/${client._id}`,
      actor: req.user ? { _id: req.user.id, name: req.user.name } : undefined,
      relatedTo: { module: "crm", recordId: client._id },
      metadata: { leadName: client.name, trackingId: client.trackingId, source: client.source },
    });

    // KIT automation trigger (fire-and-forget).
    kitEvents.emit("lead.created", {
      sourceModule: "crm",
      entityType: "lead",
      entityId: client._id,
      payload: { status: client.status, projectType: client.projectType, source: client.source },
      actor: req.user,
    });

    // Dynamic post-enquiry thank-you automation (lead + referral, WhatsApp +
    // Email). Fully admin-managed via KIT → Thank You Automation. Fire-and-forget
    // so it can never block or break enquiry creation.
    thankYouService
      .triggerThankYou(client, req.user)
      .catch((err) => console.error("[thankYou] trigger failed:", err?.message));

    return res.status(201).json({
      success: true,
      message: "Enquiry captured successfully",
      client,
      lead: client,
    });

  } catch (error) {
    console.log("Error creating client:", error.message);

    return res.status(500).json({
      success: false,
      message: error.message,
    });
  }
};

// =====================================================================
//  GET ALL CLIENTS (with filters + pagination)
// =====================================================================
const getClients = async (req, res) => {
  try {
    const page = parseInt(req.query.page) || 1;
    const limit = parseInt(req.query.limit) || 10;
    const status = req.query.status;
    const lifecycleStage = req.query.lifecycleStage;
    const projectType = req.query.projectType;

    const skip = (page - 1) * limit;

    const query = {};
    if (status) query.status = status;
    if (lifecycleStage) {
      query.lifecycleStage = lifecycleStage.includes(',')
        ? { $in: lifecycleStage.split(',').map((s) => s.trim()) }
        : lifecycleStage;
    }
    if (projectType) query.projectType = projectType;

    const clients = await CRMClient.find(query)
      .sort({ createdAt: -1 })
      .skip(skip)
      .limit(limit);

    const total = await CRMClient.countDocuments(query);

    res.status(200).json({
      message: "Clients fetched successfully",
      total,
      page,
      limit,
      clients,
      // Backward compat
      leads: clients,
    });
  } catch (error) {
    console.log("Error fetching clients:", error.message);
    res.status(500).json({ message: error.message });
  }
};

