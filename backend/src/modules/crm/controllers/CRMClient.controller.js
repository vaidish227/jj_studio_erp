//  RECORD ADVANCE PAYMENT
// =====================================================================
const recordAdvancePayment = async (req, res) => {
  try {
    const { id } = req.params;
    const { amount, note, receivedAt } = req.body;
    const client = await CRMClient.findById(id);

    if (!client) {
      return res.status(404).json({ message: "Client not found" });
    }

    client.advancePayment = {
      received: true,
      amount,
      note,
      receivedAt: receivedAt || new Date(),
      movedToProjectManagement: true,
      movedAt: new Date(),
    };
    client.lifecycleStage = "project_moved";
    client.status = "converted";

    appendInteraction(client, {
      type: "advance_payment",
      title: "Advance payment received",
      description:
        note ||
        "Advance payment recorded and marked ready for project management handoff.",
      metadata: { amount, receivedAt: receivedAt || new Date() },
    });

    await client.save();

    res.status(200).json({
      message: "Advance payment recorded successfully",
      client,
      lead: client,
    });
  } catch (error) {
    res.status(500).json({ message: error.message });
  }
};

// =====================================================================
//  CONVERT CLIENT (Mark as converted — for backward compat)
// =====================================================================
const convertClient = async (req, res) => {
  try {
    const { id } = req.params;
    const client = await CRMClient.findById(id);

    if (!client) {
      return res.status(404).json({ message: "Client not found" });
    }

    if (client.status === "converted") {
      return res.status(400).json({ message: "Client already converted" });
    }

    client.status = "converted";
    client.lifecycleStage = "converted";

    appendInteraction(client, {
      type: "status_change",
      title: "Client converted",
      description: "Client moved into the converted/project-ready stage.",
    });

    await client.save();

    res.json({
      message: "Client converted successfully",
      client,
      // Backward compat
      lead: client,
    });
  } catch (err) {
    res.status(500).json({ message: err.message });
  }
};

